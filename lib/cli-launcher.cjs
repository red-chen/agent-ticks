const fs = require('fs');
const os = require('os');
const path = require('path');
const store = require('./store.cjs');

const CODEX_HOME_ROOT = 'codex-homes';
const SYSTEM_PROMPT_FILE = 'agent-instructions.md';

function normalizeKind(kind) {
  return String(kind || '').trim().toLowerCase();
}

function isCodexAgent(agent) {
  return normalizeKind(agent.kind) === 'codex';
}

function isClaudeAgent(agent) {
  const kind = normalizeKind(agent.kind);
  return kind === 'claude' || kind === 'claudecode' || kind === 'claude-code';
}

function safeAgentId(agentId) {
  const value = String(agentId || '');
  if (!/^[a-zA-Z0-9_.-]+$/.test(value)) {
    throw new Error('Invalid agent id');
  }
  return value;
}

function codexHomeForAgent(agent) {
  return path.join(store.paths().home, CODEX_HOME_ROOT, safeAgentId(agent.id));
}

function quoteTomlString(value) {
  return JSON.stringify(String(value || ''));
}

function sandboxModeForAgent(agent) {
  const permissions = new Set((agent.permissions || []).map((item) => String(item).toLowerCase()));
  if (permissions.has('danger-full-access')) return 'danger-full-access';
  if (permissions.has('read-only') || permissions.has('readonly')) return 'read-only';
  return 'workspace-write';
}

function copyIfExists(source, destination) {
  if (!fs.existsSync(source)) return false;
  fs.rmSync(destination, { recursive: true, force: true });
  fs.mkdirSync(path.dirname(destination), { recursive: true });
  fs.cpSync(source, destination, { recursive: true });
  return true;
}

function copyAuthIfMissing(codexHome) {
  const destination = path.join(codexHome, 'auth.json');
  if (fs.existsSync(destination)) return;

  const currentHome = process.env.CODEX_HOME ? path.resolve(process.env.CODEX_HOME) : '';
  const candidates = [
    currentHome && path.join(currentHome, 'auth.json'),
    path.join(os.homedir(), '.codex', 'auth.json'),
  ].filter(Boolean);

  const source = candidates.find((candidate) => {
    try {
      return fs.existsSync(candidate) && path.resolve(candidate) !== path.resolve(destination);
    } catch {
      return false;
    }
  });
  if (source) fs.copyFileSync(source, destination);
}

function writeCodexConfig(agent, codexHome) {
  const promptPath = path.join(codexHome, SYSTEM_PROMPT_FILE);
  const systemPrompt = String(agent.systemPrompt || '').trim();
  const lines = [
    '# Generated by Agent Ticks. Edit the Agent profile instead of this file.',
    `sandbox_mode = ${quoteTomlString(sandboxModeForAgent(agent))}`,
    'approval_policy = "on-request"',
  ];

  if ((agent.systemPromptMode || 'append') === 'replace') {
    fs.writeFileSync(promptPath, `${systemPrompt}\n`);
    lines.push(`model_instructions_file = ${quoteTomlString(promptPath)}`);
  } else if (systemPrompt) {
    lines.push(`developer_instructions = ${quoteTomlString(systemPrompt)}`);
  }

  fs.writeFileSync(path.join(codexHome, 'config.toml'), `${lines.join('\n')}\n`);
}

function syncCodexAssets(agent, codexHome) {
  const agentRoot = store.agentDirectoryPath(agent.id);
  copyIfExists(path.join(agentRoot, 'skills'), path.join(codexHome, 'skills'));
  copyIfExists(path.join(agentRoot, 'plugins'), path.join(codexHome, 'plugins'));
  copyIfExists(path.join(agentRoot, 'mcp.json'), path.join(codexHome, 'mcp.json'));
}

function prepareCodexHome(agent) {
  if (!isCodexAgent(agent)) return '';

  const codexHome = codexHomeForAgent(agent);
  fs.mkdirSync(codexHome, { recursive: true });
  copyAuthIfMissing(codexHome);
  writeCodexConfig(agent, codexHome);
  syncCodexAssets(agent, codexHome);
  return codexHome;
}

function buildAgentEnv(agent, extra = {}) {
  const codexHome = isCodexAgent(agent) ? prepareCodexHome(agent) : '';
  return {
    ...extra,
    AGENT_TICKS_AGENT_ID: agent.id,
    AGENT_TICKS_AGENT_NAME: agent.name,
    AGENT_TICKS_SYSTEM_PROMPT: agent.systemPrompt,
    AGENT_TICKS_SYSTEM_PROMPT_MODE: agent.systemPromptMode || 'append',
    AGENT_TICKS_SKILLS: JSON.stringify(agent.skills || []),
    AGENT_TICKS_MCPS: JSON.stringify(agent.mcps || []),
    AGENT_TICKS_PERMISSIONS: JSON.stringify(agent.permissions || []),
    AGENT_TICKS_HOME: store.getHome(),
    AGENT_TICKS_AGENT_DIRECTORY: store.getAgentDirectory(),
    ...(codexHome ? { CODEX_HOME: codexHome, AGENT_TICKS_CODEX_HOME: codexHome } : {}),
  };
}

function buildPtyLaunch(agent, cwd) {
  if (isCodexAgent(agent)) {
    return {
      command: 'codex',
      args: ['--cd', cwd],
      env: buildAgentEnv(agent),
      isolatedHome: codexHomeForAgent(agent),
    };
  }

  return {
    command: process.env.SHELL || '/bin/zsh',
    args: [],
    env: buildAgentEnv(agent),
    isolatedHome: '',
  };
}

function buildTaskLaunch(agent, task, cwd) {
  const env = buildAgentEnv(agent, {
    AGENT_TICKS_TASK_ID: task.id,
    AGENT_TICKS_TASK_NAME: task.name,
    AGENT_TICKS_PROMPT: task.prompt,
  });

  if (isCodexAgent(agent)) {
    return {
      command: 'codex',
      args: ['exec', '--cd', cwd, '--ephemeral', task.prompt || ''],
      env,
    };
  }

  if (isClaudeAgent(agent)) {
    return {
      command: 'claude',
      args: ['-p', task.prompt || ''],
      env,
    };
  }

  return null;
}

module.exports = {
  buildAgentEnv,
  buildPtyLaunch,
  buildTaskLaunch,
  codexHomeForAgent,
  isCodexAgent,
  prepareCodexHome,
};
